<script>
  const container = document.getElementById('opinioesContainer');

  function carregarOpinioes() {
    const opinioes = JSON.parse(localStorage.getItem("opinioesSalvas")) || [];
    container.innerHTML = '';

    if (opinioes.length === 0) {
      container.innerHTML = '<p style="color: #666;">Nenhuma opinião salva ainda.</p>';
      return;
    }

    opinioes.forEach((opiniao, index) => {
      const card = document.createElement('div');
      card.style = 'margin-bottom: 20px; padding: 15px; border-radius: 10px; background-color: #f9fbfd; border: 1px solid #cce0f7;';

      card.innerHTML = `
        <p id="texto-${index}" style="white-space: pre-wrap; font-size: 16px; color: #333; margin-bottom: 12px;">${opiniao}</p>
        <textarea id="edit-${index}" style="display:none; width: 100%; height: 80px; padding: 10px; border-radius: 8px; border: 1px solid #cce0f7; resize: vertical;">${opiniao}</textarea>
        <button id="editarBtn-${index}" style="padding: 8px 18px; background-color: #4a90e2; color: white; border: none; border-radius: 8px; cursor: pointer;">Editar</button>
        <button id="salvarBtn-${index}" style="padding: 8px 18px; background-color: #27ae60; color: white; border: none; border-radius: 8px; cursor: pointer; display:none; margin-left: 10px;">Salvar</button>
        <button id="cancelarBtn-${index}" style="padding: 8px 18px; background-color: #999; color: white; border: none; border-radius: 8px; cursor: pointer; display:none; margin-left: 10px;">Cancelar</button>
        <button id="excluirBtn-${index}" style="padding: 8px 18px; background-color: #e74c3c; color: white; border: none; border-radius: 8px; cursor: pointer; margin-left: 10px;">Excluir</button>
      `;

      container.appendChild(card);

      const editarBtn = document.getElementById(`editarBtn-${index}`);
      const salvarBtn = document.getElementById(`salvarBtn-${index}`);
      const cancelarBtn = document.getElementById(`cancelarBtn-${index}`);
      const excluirBtn = document.getElementById(`excluirBtn-${index}`);
      const textoEl = document.getElementById(`texto-${index}`);
      const editEl = document.getElementById(`edit-${index}`);

      editarBtn.onclick = () => {
        textoEl.style.display = 'none';
        editEl.style.display = 'block';
        editarBtn.style.display = 'none';
        salvarBtn.style.display = 'inline-block';
        cancelarBtn.style.display = 'inline-block';
        excluirBtn.style.display = 'none';  // Esconde excluir durante edição
      };

      salvarBtn.onclick = () => {
        const novoTexto = editEl.value.trim();
        if (novoTexto === '') {
          alert('A opinião não pode ficar vazia.');
          return;
        }
        opinioes[index] = novoTexto;
        localStorage.setItem("opinioesSalvas", JSON.stringify(opinioes));
        carregarOpinioes();
      };

      cancelarBtn.onclick = () => {
        editEl.value = opinioes[index];
        textoEl.style.display = 'block';
        editEl.style.display = 'none';
        editarBtn.style.display = 'inline-block';
        salvarBtn.style.display = 'none';
        cancelarBtn.style.display = 'none';
        excluirBtn.style.display = 'inline-block';
      };

      excluirBtn.onclick = () => {
        if (confirm("Tem certeza que deseja excluir essa opinião?")) {
          opinioes.splice(index, 1);
          localStorage.setItem("opinioesSalvas", JSON.stringify(opinioes));
          carregarOpinioes();
        }
      };
    });
  }

  function adicionarOpiniao() {
    const novaOpiniaoTexto = document.getElementById('novaOpiniaoTexto');
    const texto = novaOpiniaoTexto.value.trim();
    if (texto === '') {
      alert('Digite algo para adicionar a opinião.');
      return;
    }
    let opinioes = JSON.parse(localStorage.getItem("opinioesSalvas")) || [];
    opinioes.push(texto);
    localStorage.setItem("opinioesSalvas", JSON.stringify(opinioes));
    novaOpiniaoTexto.value = '';
    carregarOpinioes();
  }

  window.onload = carregarOpinioes;
</script>

