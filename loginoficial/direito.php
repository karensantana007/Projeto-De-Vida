    <p>
      O Direito é uma ciência social aplicada que visa regulamentar o convívio entre os indivíduos e a estruturação da ordem jurídica no Estado. Ele atua como instrumento de pacificação social, normatizando condutas, resolvendo conflitos e assegurando justiça.
    </p>
    <p>
      No Brasil, o Direito encontra sua base na Constituição Federal de 1988, que estrutura os poderes da República e garante direitos fundamentais como a liberdade, a dignidade da pessoa humana e a igualdade perante a lei.
    </p>
    <p>
      Juristas como Norberto Bobbio e Hans Kelsen ajudaram a fundamentar o Direito moderno, especialmente nas áreas de positivismo jurídico e teoria da norma. Já Miguel Reale contribuiu com a Teoria Tridimensional do Direito, unindo fato, valor e norma como elementos inseparáveis do fenômeno jurídico.
    </p>
